export function findBlessing(count: number): Blessing | null {
  return BLESSINGS.find(b => b.count === count) ?? null
}

export function nearestBlessingAtOrBelow(count: number): Blessing | null {
  const candidates = BLESSINGS.filter(b => b.count <= count)
  if (!candidates.length) return null
  candidates.sort((a, b) => b.count - a.count)
  return candidates[0]
}

